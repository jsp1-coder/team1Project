package lmsTeamPro.login.service;

public class StudentUser {

	private String std_id;
	private String std_pwd;
	
	public StudentUser(String std_id, String std_pwd) {
		
		this.std_id = std_id;
		this.std_pwd = std_pwd;
	}

	public String getStaff_id() {
		return std_id;
	}
	

	public String getStaff_pwd() {
		return std_pwd;
	}

	
	
	
	
	
	

	
}

